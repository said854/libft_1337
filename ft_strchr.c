#include <stddef.h>

char	*ft_strchr(const char *p, int c)
{
	char	*s;
	size_t	i;

	s = (char *) p;
	i = 0;
	while (s[i])
	{
		if (s[i] == c)
			return (&s[i]);
		i++;
	}
	return (0);
}
